-- Supabase schema for wedding invitation data

create table if not exists public.rsvp_guests (
  id bigint generated by default as identity primary key,
  name text not null,
  email text,
  phone text,
  attendance text not null check (attendance in ('attending', 'not_attending', 'pending')),
  number_of_guests integer not null default 1,
  message text,
  created_at timestamptz not null default now()
);

create table if not exists public.guest_list (
  id bigint generated by default as identity primary key,
  name text not null,
  title text,
  whatsapp text not null,
  slug text not null unique,
  invited boolean not null default false,
  rsvp_status text not null check (rsvp_status in ('attending', 'not_attending', 'not_responded')) default 'not_responded',
  message_sent boolean not null default false,
  message_sent_at timestamptz,
  rsvp_message text,
  country text not null check (country in ('Indonesia', 'Singapore', 'United States', 'Netherlands')) default 'Indonesia',
  language text not null check (language in ('id', 'en')) default 'id',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists guest_list_slug_idx on public.guest_list (slug);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists guest_list_set_updated_at on public.guest_list;
create trigger guest_list_set_updated_at
before update on public.guest_list
for each row
execute function public.set_updated_at();

